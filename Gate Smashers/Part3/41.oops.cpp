int main() {
    // =========================================================================
// OBJECT-ORIENTED PROGRAMMING (OOP) OVERVIEW
// =========================================================================
// Definition: A paradigm that designs software around "objects."
// These objects combine data (attributes) and behavior (methods/functions).
//
// Key Advantages of OOP:
// 1. Easier to Maintain        - Localized code changes reduce system-wide bugs.
// 2. More Reusable             - Encourages modular code and component reuse.
// 3. More Scalable & Organized - Simplifies management of large, growing codebases.
// 4. Real-World Modeling       - Maps software design directly to tangible entities.
//
// -------------------------------------------------------------------------
// THE FOUR PILLARS OF OOP
// -------------------------------------------------------------------------
// 1. ENCAPSULATION
//    - Bundles data and methods into a single unit (class).
//    - Restricts direct access using access specifiers (private, protected).
//
// 2. ABSTRACTION
//    - Hides complex internal logic from the end-user.
//    - Exposes only essential interfaces (e.g., using abstract classes).
//
// 3. INHERITANCE
//    - Allows a child class to inherit properties and behaviors from a parent class.
//    - Promotes code reuse and establishes an "is-a" relationship.
//
// 4. POLYMORPHISM
//    - Allows a single interface/method to execute differently based on the object.
//    - Implemented via Compile-time (Overloading) and Runtime (Virtual functions).
// =========================================================================
// =========================================================================
// OBJECT-ORIENTED PROGRAMMING (OOP) TERMINOLOGY & CORE CONCEPTS
// =========================================================================
//
// 1. CLASS
//    - Meaning: A blueprint or template used for creating objects.
//
// 2. OBJECT
//    - Meaning: An instance of a class that contains actual data and functions.
//
// 3. ENCAPSULATION
//    - Meaning: Hides internal data and only exposes what is absolutely necessary.
//
// 4. INHERITANCE
//    - Meaning: Allows one class to inherit behavior and properties from another.
//
// 5. POLYMORPHISM
//    - Meaning: One function or method behaves differently based on input/context.
//
// 6. ABSTRACTION
//    - Meaning: Hides complex implementation details, showing only necessary features.
//
// =========================================================================

    return 0;
}
