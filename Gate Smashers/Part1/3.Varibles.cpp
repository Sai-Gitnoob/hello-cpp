/*
================================================================================
NOTES: VARIABLES & DATA TYPES IN C++ (Gate Smashers)
================================================================================
1. Definition:
   - A variable is a labeled container in memory used to store data that 
     your program can use or change.

2. Variable Naming Rules:
   - Must start with a letter (A-Z or a-z) or an underscore (_).
   - After the first letter, names can contain letters, digits (0-9), or underscores.
   - NO spaces or special characters (like @, #, !) are allowed.
   - Variable names CANNOT be a C++ keyword (e.g., int, void, return).
   - Variable names are strictly case-sensitive.

3. Concept Example:
   - Syntax: int x = 10;
   - Here, 'int' tells the compiler what kind of box to make.
   - 'x' is the name/label of that memory location.
   - The value (e.g., 10, 20, 500) sits inside that allocated box.
================================================================================
*/
