/*
===============================================================================
                       STANDARD TEMPLATE LIBRARY (STL)
===============================================================================
Definition:
STL is a collection of ready-to-use, generic classes and functions in C++ 
to handle data structures and algorithms efficiently.

What it provides:
-------------------------------------------------------------------------------
1. CONTAINERS
   - Purpose: Store and manage collections of data.
   - Examples: Dynamic arrays (vectors), stacks, queues, maps.

2. ALGORITHMS
   - Purpose: Ready-made functions for data manipulation.
   - Examples: Sorting, searching, counting, etc.

3. ITERATORS
   - Purpose: Smart pointers used to move through data structures easily.
===============================================================================
*/

#include <iostream>
#include <vector>    // Dynamic arrays
#include <stack>     // Stacks
#include <queue>     // Queues
#include <map>       // Maps
#include <algorithm> // Ready-made algorithms

using namespace std;

int main() {
    // These foundational components eliminate the need to write data 
    // structures and common operations completely from scratch.
    return 0;
}
